========================================================================================*/

--Find Total Customers by Country
SELECT 
country,
COUNT(customer_key) AS total_customers
FROM gold.dim_customers
GROUP BY country 
ORDER BY total_customers DESC

--Find the Toatla Customers By Gender
SELECT 
gender ,
COUNT(customer_key) AS total_customers
FROM gold.dim_customers
GROUP BY gender 
ORDER BY total_customers DESC

--Find the Total Products by category.
SELECT 
category,
COUNT(DISTINCT product_name) AS total_products
FROM gold.dim_products
GROUP BY category
ORDER BY total_products DESC

--What is the aaverage cost in each category.
SELECT 
category,
AVG(cost) AS average_cost
FROM gold.dim_products 
GROUP BY category
ORDER BY average_cost DESC 

--What is the Total Revenue generated for each category?
SELECT 
p.category,
SUM(f.sales_amount) AS total_revenue
FROM 
gold.dim_products p
JOIN gold.fact_sales f
ON p.product_key= f.product_key 
GROUP BY p.category
ORDER BY total_revenue DESC

--What is the Total Revenue generated by each customer?
SELECT 
DISTINCT c.customer_key,
c.firtsname,
c.lastname,
SUM(f.sales_amount) AS total_revenue
FROM 
gold.dim_customers c
JOIN gold.fact_sales f
ON c.customer_key= f.customer_key
GROUP BY 
c.customer_key,
c.firtsname,
c.lastname
ORDER BY total_revenue DESC

--What is the distribution of sold items across countries?
SELECT 
c.country,
SUM(f.quantity) AS total_revenue
FROM 
gold.dim_customers c
JOIN gold.fact_sales f
ON c.customer_key= f.customer_key
GROUP BY 
c.country
ORDER BY total_revenue DESC

/*======================================================================================
                                       RANKING
========================================================================================*/

--Which 5 Products generate the Highest revenue?
SELECT 
p.product_name,
SUM(f.sales_amount) AS total_revenue
FROM 
gold.dim_products p
JOIN gold.fact_sales f
ON p.product_key= f.product_key
GROUP BY 
p.product_name
ORDER BY total_revenue DESC

--Which are the tp 5 worst-performing products in terms of slaes?
SELECT TOP 5 
p.product_name,
SUM(f.sales_amount) AS total_revenue
FROM 
gold.dim_products p
JOIN gold.fact_sales f
ON p.product_key= f.product_key
GROUP BY 
p.product_name
ORDER BY total_revenue ASC

--Top 10 customers who have generated the highest revenue.
