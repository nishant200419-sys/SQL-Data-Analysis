/*======================================================================================
                                 DATE EXPLORATION
========================================================================================*/

--Find the Date of the First and Last Order.
--How many Years/Months of Sales are available.

SELECT 
MIN(order_date) AS first_order_date,
MAX(order_date) AS last_order_date,
DATEDIFF(month, MIN(order_date), MAX(order_date)) AS order_range_years  
FROM gold.fact_sales

--Find the Youngest and Oldest customer.
SELECT 
MIN(birthday) AS oldest_birthdate,
DATEDIFF(year, MIN(birthday), GETDATE()) AS oldest_customer,
MAX(birthday) AS latest_birthdate,
DATEDIFF(year, MAX(birthday), GETDATE()) AS youngest_customer
FROM gold.dim_customers 

/*======================================================================================
                                 MEASURES EXPLORATION
========================================================================================*/

--Find the Total Sales
SELECT SUM(sales_amount) AS total_sales
FROM gold.fact_sales AS total_sales

--How many items are sold.
SELECT COUNT(quantity) AS total_quantity
FROM gold.fact_sales

--Find the Average selling Price.
SELECT AVG(price) AS average_selling_price
FROM gold.fact_sales

--Find the Total Number of Orders.
SELECT COUNT(DISTINCT order_number) AS total_orders
FROM gold.fact_sales

--Find the Total Number of Customers.
SELECT COUNT(customer_key) AS total_customers
FROM gold.dim_customers

--Find the total Number of Customers that have placed an Order.
SELECT COUNT(DISTINCT customer_key) AS total_customers
FROM gold.dim_customers

--Generate a report that shoes all key metrics of the business.
SELECT 'Total Sales' AS measure_name, SUM(sales_amount) AS measure_value FROM gold.fact_sales 
UNION ALL
SELECT 'Total Qunatity', SUM(quantity) FROM gold.fact_sales 
UNION ALL
SELECT 'Average Price', AVG(price) FROM gold.fact_sales
UNION ALL
SELECT 'Total no. of orders', COUNT(DISTINCT order_date) FROM gold.fact_sales
UNION ALL
SELECT 'Total no. of products', COUNT(product_name) FROM gold.dim_products
UNION ALL
SELECT 'Total no. of customers', COUNT(customer_key) FROM gold.dim_customers

/*======================================================================================
